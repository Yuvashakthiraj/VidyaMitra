// Generate forecast data (Feb 2026 - Dec 2031)
export function generateForecast(skillName: string, currentDemand: number, growthRate: number): Array<{ period: string; demand_pct: number }> {
  const forecast = [];
  const periods = [
    'Mar 2026', 'Jun 2026', 'Sep 2026', 'Dec 2026',
    'Mar 2027', 'Jun 2027', 'Sep 2027', 'Dec 2027',
    'Mar 2028', 'Jun 2028', 'Sep 2028', 'Dec 2028',
    'Mar 2029', 'Jun 2029', 'Sep 2029', 'Dec 2029',
    'Mar 2030', 'Jun 2030', 'Sep 2030', 'Dec 2030',
    'Mar 2031', 'Jun 2031', 'Sep 2031', 'Dec 2031',
  ];
  
  let demand = currentDemand;
  const quarterlyGrowth = growthRate / 4; // Quarterly growth rate
  
  for (const period of periods) {
    demand = demand * (1 + quarterlyGrowth / 100);
    // Add some realistic variance
    const variance = (Math.random() - 0.5) * 0.5;
    forecast.push({
      period,
      demand_pct: Math.max(0, Math.min(100, demand + variance))
    });
  }
  
  return forecast;
}
